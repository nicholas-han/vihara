"""Deterministic journal generation: canonical CSVs -> ledger/generated/**.

``build_files()`` is pure: it reads the portfolio CSVs and the bridge
mapping and returns {relative path -> file text}. ``generate()`` wholesale
replaces the ``ledger/generated/`` directory with that output. Running it
twice on unchanged inputs produces byte-identical files — idempotency by
determinism; the vihara-data git diff is the review surface.

Every generated transaction carries backlink metadata (``trade_id:`` /
``row_hash:`` / ``source:`` file:line) and a ``^t.<id>`` link. Realized
P&L numbers come from records.cost_basis (the single computation owner);
the ledger's Sigma=0 check re-verifies them at load time.

Sign/shape conventions (see docs/20 in the ledger module):
  buy       Positions +qty {{qty*price+fee, date, "t:ID"}} / Cash -(total)
  sell      Positions -take {lot_date, "t:LOTID"} per consumed lot @ price
            / Cash +(qty*price-fee) / PnL -(realized)
  dividend  Cash +net / Withholding +tax / Dividends -(gross)
  cashflow  Cash +amount / counter -(amount)
  opening   Positions +qty {{qty*avg_cost, as_of, "t:opening"}} / Equity:Opening
Checkpoints become balance assertions dated as_of + 1 day (assertions hold
at the start of a day; checkpoints include the day's trades).
"""

